    private void setLech() {
		// TODO Auto-generated method stub
    	if(txtTongTienThucTe.getText().isEmpty())
    		ca.setTongTienThucTe(0);
    	else
    		ca.setTongTienThucTe(Double.parseDouble(txtTongTienThucTe.getText().replaceAll(",", "")));
    	ca.setTongTien(Double.parseDouble(txtTongTien.getText().replaceAll(",", "")));
    	
    	txtLech.setText(Formater.decimalFormat(ca.tinhTienChenhLech()));
    	if(ca.tinhTienChenhLech()!=0)
    		txtLech.setForeground(Color.red);
	}

	public void inputNumber() {
    	DocumentFilter filter = new DocumentFilter() {
    	    @Override
    	    public void insertString(FilterBypass fb, int offset, String string, AttributeSet attr) throws BadLocationException {
    	        if (string.matches("[0-9]*")) {
    	            super.insertString(fb, offset, string, attr);
    	        }
    	    }

    	    @Override
    	    public void replace(FilterBypass fb, int offset, int length, String text, AttributeSet attrs) throws BadLocationException {
    	        if (text.matches("[0-9]*")) {
    	            super.replace(fb, offset, length, text, attrs);
    	        }
    	    }
    	};
    	((PlainDocument) txtTongTienThucTe.getDocument()).setDocumentFilter(filter);
    }
