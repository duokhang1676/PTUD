        // TODO add your handling code here:
    	setLech();
    	if(ca.tinhTienChenhLech()!=0) {
    		int response = JOptionPane.showConfirmDialog(null, "Xác nhận lệch tiền!", "Xác nhận", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        	if(response!=JOptionPane.YES_OPTION)
        		return;
        	
    	}
    	ca.setTenCa(cboTenCa.getSelectedItem().toString());
		ca.setThoiGianKetCa(LocalDateTime.now());
		ca.setTrangThai(true);
		ca.setGhiChu(txtGhiChu2.getText());
		new Ca_DAO().updateCa(ca);
		LoginInfo.disposeRootframe();
        new DangNhap().setVisible(true);
    	
